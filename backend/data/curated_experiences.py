def get_curated_experiences(
    city: str = "Bangalore",
    categories: Optional[List[str]] = None,
    budget_min: int = 0,
    budget_max: int = 10000,
    solo_friendly_only: bool = False,
    limit: int = 25,
) -> List[dict]:
    """
    Get filtered curated experiences.
    
    Args:
        city: Target city (currently only Bangalore supported)
        categories: Filter by categories
        budget_min: Minimum budget in INR
        budget_max: Maximum budget in INR
        solo_friendly_only: Only return solo-friendly experiences
        limit: Maximum number of experiences to return
    
    Returns:
        List of filtered experiences
    """
    if city.lower() != "bangalore":
        return []  # Only Bangalore data available
    
    results = CURATED_EXPERIENCES.copy()
    
    # Filter by categories
    if categories:
        category_lower = [c.lower() for c in categories]
        results = [
            exp for exp in results
            if exp["category"].lower() in category_lower
        ]
    
    # Filter by budget
    results = [
        exp for exp in results
        if exp["budget"]["min"] >= budget_min and exp["budget"]["max"] <= budget_max
    ]
    
    # Filter by solo-friendly
    if solo_friendly_only:
        results = [
            exp for exp in results
            if exp["solo_friendly"]["is_solo_sure"]
        ]
    
    return results[:limit]
